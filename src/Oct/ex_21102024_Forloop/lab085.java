package Oct.ex_21102024_Forloop;

public class lab085 {
    public static void main(String[] args) {
        for (int _1 = 0; _1 <= 10; _1++) {
            System.out.println(_1);

            //int _1 = 0 = initialization
            // _1<=10 = condition
            //_1++ = updation
        }
        for (int i = 0; i < 10; ++i) {
            System.out.println(i);

            //for loop r jonno pre (++i) or post (i++) both are same
        }
    }
}
