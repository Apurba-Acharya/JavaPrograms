package Oct.ex_18102024_switch;

public class lab083 {
    public static void main(String[] args) {
        char code = 'E';
        switch (code) {
            default:
                System.out.println("default");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
