package Oct.ex_15102024_increment_decrement;

public class lab062 {
    public static void main(String[] args) {
        //Pre - increment - ++operand.
        int a = 10;
        int b = ++a;
        System.out.println(++a);
        System.out.println(b);

        //post >>
        int p = 10;
        System.out.println(p++);
        System.out.println(p);
    }
}
